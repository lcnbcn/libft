#include "libft.h"

int	ft_atoi(const char *s)
{
	int	i;
	int	vpn;

	i = 0;
	vpn = 1;
	while (*s == 32 || (*s >= 9 && *s <= 13))
		s++;
	if (*s == 45)
		vpn *= -1;
	if (*s == 43 || *s == 45)
		s++;
	while (ft_isdigit(*s))
	{
		i = i * 10 + *s - 48;
		s++;
	}
	return (i * vpn);
}
