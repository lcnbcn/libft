#include <stdio.h>

int	ft_atoi(const char *str)
{
	int	i;
	int	j;
	int	h;

	h = 0;
	i = 0;
	j = 1;
	while (str[h] == 32 || (str[h] >= 9 && str[h] <= 13))
	{
		h++;
	}
	while (str[h] == '-' || str[h] == '+')
	{
		if (str[h] == '-')
			j *= -1;
		h++;
	}
	while (str[h] >= '0' && str[h] <= '9')
	{
		i = (i * 10) + (str[h] - '0');
		h++;
	}
	return (i * j);
}
